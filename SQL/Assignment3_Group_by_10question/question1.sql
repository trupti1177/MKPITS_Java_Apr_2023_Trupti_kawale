-- 1. Retrieve the total revenue generated for each film category.
use sakila

select * from film_category -- film_id
select * from film -- film_id, 
select * from inventory -- film_id, inventory_id
select * from rental -- inventory_id, rental_id
select * from payment -- rental_id

select film_category.category_id, sum(amount) as "revenue generated by each film category" from film_category join film on
film_category.film_id = film.film_id join inventory on
film.film_id = inventory.film_id join rental on
inventory.inventory_id = rental.inventory_id join payment on
rental.rental_id = payment.payment_id group by category_id

select film_category.category_id, sum(amount) as "revenue generated by each film category" from payment join rental on
payment.rental_id = rental.rental_id join inventory on
rental.inventory_id = inventory.inventory_id join film on
inventory.film_id = film.film_id join film_category on
film.film_id = film_category.film_id group by category_id