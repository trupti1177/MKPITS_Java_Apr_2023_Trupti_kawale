public class SwitchCseDefaultValue {

        public static  void main(String[] args){
            int name = 7;
            switch (name) {
                case 1:
                    System.out.println("Teju");
                    break;
                case 2:
                    System.out.println("Trupti");
                    break;
                case 3:
                    System.out.println("Shreyash");
                    break;
                default:
                    System.out.println("Pradnya");
                case 4:
                    System.out.println("Shubham");
                    break;
                case 5:
                    System.out.println("Pranay");
                    break;
                case 6:
                    System.out.println("Abhishek");
                    break;
                case 7:
                    System.out.println("Mrunali");
                    break;
            }
        }
    }

